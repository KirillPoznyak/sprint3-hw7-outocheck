function capitalizeWords(string) {
  const arrString = string.split('');
  for (let i = 0; i < arrString.length; i++) {
    if (i === 0 || arrString[i-1] === ' ') {
      arrString[i] = arrString[i].toUpperCase();
    }
  }
  let result = arrString.join('');
  return result;
}

